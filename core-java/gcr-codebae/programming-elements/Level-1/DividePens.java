public class DividePens {
     public static void main(String[] args) {
        int Pens=14;
        int Students=3;
        int ans=Pens/Students;
        int rem=Pens%Students;
        System.out.println("Pen Distributed to each Students: " + ans);
        System.out.println("Remaining Pens: " + rem);
     }
}
